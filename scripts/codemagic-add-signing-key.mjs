// scripts/codemagic-add-signing-key.mjs
//
// Generates an RSA 2048 private key, uploads it to Codemagic's
// haloframe_secrets env-var group as CERTIFICATE_PRIVATE_KEY (secure),
// and saves a local backup to .env.codemagic.local. Idempotent — if the
// var already exists in Codemagic, this prints the existing id and
// exits cleanly without rotating.
//
// Why we need this:
//   app-store-connect fetch-signing-files "$BUNDLE_ID" --type IOS_APP_STORE
//     --create requires a private key as input (to generate the CSR Apple
//     signs into a cert). Codemagic CLI does NOT auto-generate one in
//     non-interactive mode — without --certificate-key, the build fails
//     with "Cannot save Signing Certificates without certificate private key".
//
// Reusing the same key across builds keeps Apple issuing the SAME
// distribution cert each build (rather than burning through Apple's
// per-team cert quota).

import { readFileSync, writeFileSync, appendFileSync, existsSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { generateKeyPairSync } from 'node:crypto';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const ENV_FILE = resolve(ROOT, '.env.codemagic.local');
const APP_ID = '69f50db033172bbb569e2285';
const GROUP = 'haloframe_secrets';
const VAR_KEY = 'CERTIFICATE_PRIVATE_KEY';

function loadEnv() {
  const out = {};
  const text = readFileSync(ENV_FILE, 'utf8');
  const lines = text.split('\n');
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (!line || line.startsWith('#')) continue;
    const m = line.match(/^([A-Z_]+)=(.*)$/);
    if (!m) continue;
    const [, key, rawVal] = m;
    let val = rawVal;
    if (val.startsWith('"') && !val.endsWith('"')) {
      while (i + 1 < lines.length && !lines[i + 1].endsWith('"')) {
        i++; val += '\n' + lines[i];
      }
      if (i + 1 < lines.length) { i++; val += '\n' + lines[i]; }
    }
    if (val.startsWith('"') && val.endsWith('"')) val = val.slice(1, -1);
    out[key] = val;
  }
  return out;
}

const env = loadEnv();
const token = env.CODEMAGIC_API_TOKEN;
if (!token) {
  console.error('CODEMAGIC_API_TOKEN not found in .env.codemagic.local');
  process.exit(2);
}

async function cm(path, init = {}) {
  const r = await fetch(`https://api.codemagic.io${path}`, {
    ...init,
    headers: { 'x-auth-token': token, 'content-type': 'application/json', ...(init.headers || {}) },
  });
  const text = await r.text();
  if (!r.ok) throw new Error(`${r.status} ${path}\n${text.slice(0, 500)}`);
  return text ? JSON.parse(text) : {};
}

// 1. Check if CERTIFICATE_PRIVATE_KEY already exists in Codemagic
const allVars = await cm(`/apps/${APP_ID}/variables`);
const existing = allVars.find(v => v.key === VAR_KEY);
if (existing) {
  console.log(`✓ ${VAR_KEY} already exists in Codemagic (id=${existing.id}, group=${existing.group}, secure=${existing.secure})`);
  console.log('  No changes made. Re-running this script is safe — it idempotently exits when the var exists.');
  console.log('  To rotate the key: delete it in Codemagic dashboard first, then re-run.');
  process.exit(0);
}

console.log('Generating new RSA 2048 private key...');
const { privateKey } = generateKeyPairSync('rsa', {
  modulusLength: 2048,
  publicKeyEncoding: { type: 'spki', format: 'pem' },
  privateKeyEncoding: { type: 'pkcs8', format: 'pem' },
});

console.log('Uploading to Codemagic...');
const created = await cm(`/apps/${APP_ID}/variables`, {
  method: 'POST',
  body: JSON.stringify({
    key: VAR_KEY,
    value: privateKey,
    secure: true,
    group: GROUP,
  }),
});
console.log(`✓ Created CERTIFICATE_PRIVATE_KEY in Codemagic (id=${created.id}, group=${created.group}, secure=${created.secure})`);

// 2. Save backup to .env.codemagic.local (gitignored)
const envContent = readFileSync(ENV_FILE, 'utf8');
if (!envContent.includes(VAR_KEY + '=')) {
  const block = `\n# iOS code-signing RSA private key. Used by Codemagic's\n# \`app-store-connect fetch-signing-files --create --certificate-key\`\n# step to mint the iOS distribution cert. Generated by\n# scripts/codemagic-add-signing-key.mjs. Mirror lives in Codemagic\n# env-var group "${GROUP}" as ${VAR_KEY} (secure).\n${VAR_KEY}="${privateKey.trim()}"\n`;
  appendFileSync(ENV_FILE, block);
  console.log('✓ Saved local backup to .env.codemagic.local');
} else {
  console.log('  (Local .env.codemagic.local already had a CERTIFICATE_PRIVATE_KEY entry — left as-is.)');
}

console.log('\nDone. Now update codemagic.yaml Step "Fetch + create signing files" to pass the key:');
console.log('    --certificate-key=@env:CERTIFICATE_PRIVATE_KEY');
